
let n1 = prompt("Digite o primeiro valor")
let n2 = prompt("Digite o segundo valor")
let n3 = prompt("Digite o terceiro valor")
let n4 = prompt("Digite o quarto valor")
let n5 = prompt("Digite o quinto valor")

let soma = parseInt(n1) + parseInt(n2) + parseInt(n3) + parseInt(n4) + parseInt(n5);

document.write(`Os números digitados foram ${n1}, ${n2}, ${n3}, ${n4} e ${n5}. A soma de todos os números é ${soma}.`)
